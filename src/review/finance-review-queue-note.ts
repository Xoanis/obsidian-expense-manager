export function buildFinanceReviewQueueNoteContent(transactionsFolder: string): string {
	return [
		'# Finance Review Queue',
		'',
		'> Review finance candidates that are waiting for confirmation or still need manual attention.',
		'',
		'## Queue Summary',
		'',
		'```dataviewjs',
		`const records = dv.pages('"${transactionsFolder}"').where((page) => ["pending-approval", "needs-attention"].includes(String(page.status ?? "")));`,
		'const pending = records.where((page) => String(page.status ?? "") === "pending-approval");',
		'const attention = records.where((page) => String(page.status ?? "") === "needs-attention");',
		'dv.table(["Queue", "Count"], [',
		'  ["Pending approval", String(pending.length)],',
		'  ["Needs attention", String(attention.length)],',
		'  ["Total", String(records.length)],',
		']);',
		'```',
		'',
		'## Pending Approval',
		'',
		'```dataview',
		'TABLE file.link AS Record, dateTime, amount, currency, description, category, source, artifact',
		`FROM "${transactionsFolder}"`,
		'WHERE status = "pending-approval"',
		'SORT dateTime DESC',
		'```',
		'',
		'## Needs Attention',
		'',
		'```dataview',
		'TABLE file.link AS Record, dateTime, description, source, artifact',
		`FROM "${transactionsFolder}"`,
		'WHERE status = "needs-attention"',
		'SORT dateTime DESC',
		'```',
		'',
	].join('\n');
}
